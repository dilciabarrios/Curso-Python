#COMPROBAR SI DOS ELEMENTOS SON IGUALES

# ==

print ('¿Es 5==5?', 5==5)
print ('¿Es patata == cebolla?', 'patata' =='cebolla')

#COMPROBAR SI DOS ELEMENTOS SON DIFERENTES !=

print ('¿Es 5!=5?', 5!=5)
print ('¿Es patata != cebolla?', 'patata' !='cebolla')

#COMPROBACIONES ARITMETICAS: MAYOR QUE O IGUAL QUE, MENOR QUE O MENOR O IGUAL QUE
# >, >= <, <=

print ('¿Es 5>2?', 5>2)
print ('¿Es 5>=10?', 5>=10)
print ('¿Es 5<2?', 5<2)
print ('¿Es 5<=10?', 5<=10)

#COMPROBACION "in" - ¿esta algo de dentro de otra cosa?
miString = 'abcdefgh'
print ('¿Esta b dentro de miString?', 'b' in miString)

miNuevaString = 'Mi amigo José tiene un pato que se llama Filiberto'
print ('¿Esta pato dentro de miNuevaString?', 'pato' in miNuevaString)

